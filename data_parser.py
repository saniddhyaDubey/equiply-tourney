def decode_linet(serial):
    s = serial.strip()

    match = re.match(r'^(20\d{2})', s)
    if match:
        return match.group(1)

    return None

def decode_stryker(serial):
    s = serial.strip()

    # standard: starts with full YYYY
    match = re.match(r'^(20\d{2})', s)
    if match:
        return match.group(1)

    # fallback: 2-digit year at start (e.g. 109035466 → 10 → 2010)
    match2 = re.match(r'^(\d{2})\d', s)
    if match2:
        yy = int(match2.group(1))
        if 0 <= yy <= 30:
            return str(2000 + yy)

    return None

def decode_edan(serial):
    s = re.sub(r'^\(21\)', '', serial.strip()).strip()

    # find M or K followed by exactly 2 digits
    match = re.search(r'[MK](\d{2})', s)
    if match:
        yy = int(match.group(1))
        if 0 <= yy <= 30:  # sanity check
            return str(2000 + yy)

    return None

def decode_hillrom(serial):
    s = serial.strip()

    # CENTURY, CENTURYP1400, PCENTURYK3256 — year is last 4 digits
    match = re.search(r'(19\d{2}|20\d{2})$', s)
    if match:
        return match.group(1)

    # P3200 — [Letter][YY] at start
    match2 = re.match(r'^[A-Z](\d{2})', s)
    if match2:
        return str(2000 + int(match2.group(1)))

    return None

def decode_zoll(serial):
    MONTH_MAP = {
        'A': 'January', 'B': 'February', 'C': 'March', 'D': 'April',
        'E': 'May', 'F': 'June', 'G': 'July', 'H': 'August',
        'I': 'September', 'J': 'October', 'K': 'November', 'L': 'December'
    }

    cleaned = re.sub(r'^\(21\)\s*', '', serial.strip()).upper()
    cleaned = re.sub(r'^\d([A-Z])', r'\1', cleaned)

    # standard: [1-2 letter product code][2-digit year][month letter]
    match = re.match(r'^([A-Z]{1,2})(\d{2})([A-Z])', cleaned)
    if match:
        month_char = match.group(3)
        if month_char in MONTH_MAP:
            year = 2000 + int(match.group(2))
            month = MONTH_MAP[month_char]
            return f"{month} {year}"

    # fallback: single digit year (e.g. t1k → T1K → 2001)
    match2 = re.match(r'^([A-Z]{1,2})(\d)([A-Z])', cleaned)
    if match2:
        month_char = match2.group(3)
        if month_char in MONTH_MAP:
            year = 2000 + int(match2.group(2))
            month = MONTH_MAP[month_char]
            return f"{month} {year}"

    return None

