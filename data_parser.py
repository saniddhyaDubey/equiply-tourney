    # SA3YY pattern (e.g. SA315219009 → 2015)
    match = re.match(r'^SA3(\d{2})', s)
    if match:
        return str(2000 + int(match.group(1)))

    # SPX YY pattern (e.g. SPX18510114SA → 2018)
    match = re.match(r'^SPX(\d{2})', s)
    if match:
        return str(2000 + int(match.group(1)))

    # RTS YY pattern (e.g. RTS14024388GA → 2014)
    match = re.match(r'^RTS(\d{2})', s)
    if match:
        return str(2000 + int(match.group(1)))

    # RT9YY pattern (e.g. RT916162191SA → 2016, RT908362478GA → 2008)
    match = re.match(r'^RT9(\d{2})', s)
    if match:
        return str(2000 + int(match.group(1)))

    return None

